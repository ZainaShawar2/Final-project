    private void enter__Button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enter__Button1ActionPerformed
        // TODO add your handling code here:

        int NoOfBooks = Integer.parseInt(numberofbookstoborrow_TextField2.getText());
        int studentId = Integer.parseInt(IDstudent_TextField2.getText());
        Student student = students.stream().filter((t) -> {
            if (t.getId() == studentId) {
                return true;
            }
            return false;
        }).findFirst().get();

        if (NoOfBooks > 3) {
            JOptionPane.showMessageDialog(this, "you cannot borrow more than 3 books!");
        } else if (student.getBorrowedBooks().size() + NoOfBooks > 3) {
            JOptionPane.showMessageDialog(this, "you exceeded the number of allowed borrowed books");
        } else {
            for (int i = 0; i < NoOfBooks; i++) {
                String bookTitle = JOptionPane.showInputDialog("Enter book name");
                Book wantedBook = null;
                for (Book book : books) {
                    if (book.getTitle().equals(bookTitle) && !book.isIsLoaned()) {
                        wantedBook = book;
                        break;
                    }
                }
                student.addLoadnedBook(wantedBook);
               
            }
            
        }
        IDstudent_TextField2.setText("");
        numberofbookstoborrow_TextField2.setText("");
        numberofbookstoborrow_TextField2.setEnabled(false);
        
        
    }//GEN-LAST:event_enter__Button1ActionPerformed
    void showBooks() {
        String[] data = new String[books.size()];
        for (int i = 0; i < books.size(); i++) {
            Book b = books.get(i);
            data[i] = (i + 1) + "-" + b.getClass().getSimpleName() + " --> " + b.getInfo();
        }
         Books_List2.setListData(data);
         
    }

    

    void showStudents() {

        String[] data = new String[students.size()];
        for (int i = 0; i < students.size(); i++) {
            Student s = students.get(i);
            data[i] = (i + 1) + "-" + s.getInfo();
        }
        STUDENTS_List1.setListData(data);

    }

