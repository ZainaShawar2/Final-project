    private void enter_Button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enter_Button3ActionPerformed
        // TODO add your handling code here:

        try {

            int loanday = Integer.parseInt(returnDAY_TextField3.getText());
            int loanmonth = Integer.parseInt(returnMONTH_TextField4.getText());
            int loanyear = Integer.parseInt(returnYEAR_TextField5.getText());
            int res = loanday + 5 - 30;

            int bookreturnday = Integer.parseInt(thebookRETURNEDDAY_TextField6.getText());
            int bookreturnmonth = Integer.parseInt(thebookRETURNEDMONTH_TextField4.getText());
            int bookreturnyear = Integer.parseInt(thebookRETURNEDYEAR_TextField5.getText());

            if (bookreturnmonth == loanmonth && bookreturnyear == loanyear) {
                if (bookreturnday > (loanday + 5)) {
                    JOptionPane.showMessageDialog(this, "You have exceeded the loan period!");
                } else {
                    JOptionPane.showMessageDialog(this, "You returned the book in time!");
                }
            } else if (bookreturnyear > loanyear) {
                if (bookreturnmonth > 1) {
                    JOptionPane.showMessageDialog(this, "you have exceeded the loan period!!");
                } else if (bookreturnmonth == 1 && bookreturnday <= res) {
                    JOptionPane.showMessageDialog(this, "you returned the book in time!");
                } else {
                    JOptionPane.showMessageDialog(this, "you have exceeded the loan period!");
                }
            } else if ((bookreturnyear == loanyear) && (bookreturnmonth == loanmonth + 1) && (bookreturnday > res)) {
                JOptionPane.showMessageDialog(this, "You have exceeded the loan book period!!");
            } else {
                JOptionPane.showMessageDialog(this, "You returned the book in time!!");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "please enter a valid date!");
        }

    }//GEN-LAST:event_enter_Button3ActionPerformed

